def calculate_metrics(y_true, y_pred):
    """Calculate RMSE, MAE, R2, PCC"""
    rmse = np.sqrt(np.mean((y_true - y_pred) ** 2))
    mae = np.mean(np.abs(y_true - y_pred))

    ss_res = np.sum((y_true - y_pred) ** 2)
    ss_tot = np.sum((y_true - np.mean(y_true)) ** 2)
    r2 = 1 - (ss_res / (ss_tot + 1e-8))

    vx = y_true - np.mean(y_true)
    vy = y_pred - np.mean(y_pred)
    pcc = np.sum(vx * vy) / (np.sqrt(np.sum(vx ** 2)) * np.sqrt(np.sum(vy ** 2)) + 1e-8)

    return rmse, mae, r2, pcc


def inverse_normalize(tensor, var_name='Label'):
    # If Label statistics don't exist, try T2m, or return directly
    if var_name not in PHYSICAL_RANGES:
        if var_name == 'Label' and 'T2m' in PHYSICAL_RANGES:
            var_name = 'T2m'
        else:
            return tensor
    min_val, max_val = PHYSICAL_RANGES[var_name]
    return tensor * (max_val - min_val) + min_val


def save_geotiff(path, data, profile, nodata_value=-9999.0):
    profile.update({
        'driver': 'GTiff', 'dtype': 'float32', 'count': 1,
        'compress': 'lzw', 'nodata': nodata_value
    })
    data_filled = np.nan_to_num(data, nan=nodata_value)
    with rasterio.open(path, 'w', **profile) as dst:
        dst.write(data_filled.astype(np.float32), 1)


def plot_station_scatter(obs, pred, save_path):
    """Plot station validation scatter"""
    valid_mask = ~np.isnan(obs) & ~np.isnan(pred)
    y_true, y_pred = obs[valid_mask], pred[valid_mask]

    if len(y_true) == 0: return

    rmse, mae, r2, pcc = calculate_metrics(y_true, y_pred)

    # Downsample for plotting
    plot_idx = np.random.choice(len(y_true), min(10000, len(y_true)), replace=False)
    x, y = y_true[plot_idx], y_pred[plot_idx]

    # Calculate density
    xy = np.vstack([x, y])
    try:
        z = gaussian_kde(xy)(xy)
        idx = z.argsort()
        x, y, z = x[idx], y[idx], z[idx]
    except:
        z = np.ones_like(x)

    plt.figure(figsize=(10, 9))
    plt.scatter(x, y, c=z, s=20, cmap='Spectral_r', alpha=0.8)

    # Reference line
    limits = [-10, 45]
    plt.plot(limits, limits, 'r-', lw=2, label='1:1 Line')

    # Fit line
    coef = np.polyfit(y_true, y_pred, 1)
    poly1d_fn = np.poly1d(coef)
    plt.plot(limits, poly1d_fn(limits), 'k--', lw=2, label='Fit Line')

    plt.xlim(limits);
    plt.ylim(limits)
    plt.xlabel("Observed T2m (°C)", fontsize=20)
    plt.ylabel("Predicted T2m (°C)", fontsize=20)

    stats_text = f'RMSE={rmse:.2f}\nMAE={mae:.2f}\nR²={r2:.2f}\nPCC={pcc:.2f}'
    plt.gca().text(0.05, 0.95, stats_text, transform=plt.gca().transAxes,
                   fontsize=18, verticalalignment='top',
                   bbox=dict(boxstyle='round', facecolor='white', alpha=0.8))

    plt.grid(True, linestyle='--', alpha=0.5)
    plt.tight_layout()
    plt.savefig(save_path, dpi=300)
    plt.close()


def save_visualization(date_str, pred, target, save_dir):
    """Plot simple spatial comparison"""
    pred_img = pred.squeeze().cpu().numpy()
    tar_img = target.squeeze().cpu().numpy()

    plt.figure(figsize=(12, 4))

    vmin, vmax = np.nanmin(tar_img), np.nanmax(tar_img)

    plt.subplot(1, 3, 1)
    plt.imshow(pred_img, cmap='jet', vmin=vmin, vmax=vmax)
    plt.title("Prediction")
    plt.axis('off')

    plt.subplot(1, 3, 2)
    plt.imshow(tar_img, cmap='jet', vmin=vmin, vmax=vmax)
    plt.title("Target (RF)")
    plt.axis('off')

    plt.subplot(1, 3, 3)
    diff = np.abs(pred_img - tar_img)
    plt.imshow(diff, cmap='coolwarm', vmin=0, vmax=3)
    plt.title("Abs Error")
    plt.axis('off')
    plt.colorbar()

    plt.tight_layout()
    plt.savefig(os.path.join(save_dir, f"{date_str}_compare.png"), dpi=150)
    plt.close()


