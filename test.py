def main_test():
    if not os.path.exists(OUTPUT_DIR): os.makedirs(OUTPUT_DIR)
    if not os.path.exists(TIF_OUTPUT_DIR): os.makedirs(TIF_OUTPUT_DIR)

    device = torch.device(f'cuda:{GPU_ID}' if torch.cuda.is_available() else 'cpu')
    print(f"🚀 Testing on device: {device}")

    # --- Read DEM to generate Valid Mask ---
    print(f"Loading Valid Mask from DEM...")
    dem_path = data_loader.DATA_PATHS['DEM']
    with rasterio.open(dem_path) as src:
        ref_profile = src.profile.copy()
        dem_data = src.read(1)
        valid_mask_numpy = (dem_data != src.nodata) & (dem_data > -1e30)

    # --- Load dataset ---
    # When is_train=False, Dataset returns full image data
    test_dataset = CausalDownscalingDataset(
        start_date=TEST_START, end_date=TEST_END, patch_size=FALLBACK_PATCH_SIZE, is_train=False
    )
    test_loader = DataLoader(test_dataset, batch_size=1, shuffle=False, num_workers=0)

    # --- Load model ---
    print("Loading Model...")
    model = PCI_SFTNet(pcmci_json_path=PCMCI_JSON_PATH).to(device)

    if os.path.exists(CHECKPOINT_PATH):
        checkpoint = torch.load(CHECKPOINT_PATH, map_location=device)
        state_dict = checkpoint if 'module.' not in list(checkpoint.keys())[0] else \
            OrderedDict({k.replace('module.', ''): v for k, v in checkpoint.items()})
        model.load_state_dict(state_dict)
        print(f"✅ Loaded weights: {CHECKPOINT_PATH}")
    else:
        print(f"❌ Checkpoint not found: {CHECKPOINT_PATH}")
        return

    # --- Statistics containers ---
    grid_metrics = {'rmse': [], 'mae': [], 'r2': []}
    station_data = {'obs': [], 'pred': []}

    print(f"Starting Inference ({len(test_dataset)} days)...")

    with torch.no_grad():
        for i, batch in enumerate(tqdm(test_loader)):
            current_date = test_dataset.dates[i].strftime("%Y%m%d")
            if 'Label' not in batch: continue

            # --- 🔥 Mixed Inference Logic 🔥 ---
            try:
                # 1. Prioritize full-image inference
                # print(f"Trying Full-Image Inference for {current_date}...")
                pred_norm = full_image_inference(model, batch, device)

            except RuntimeError as e:
                if 'out of memory' in str(e):
                    # 2. If OOM occurs, clear cache and switch to sliding window
                    # print(f"⚠️ OOM detected! Switching to Sliding Window (Patch={FALLBACK_PATCH_SIZE})...")
                    torch.cuda.empty_cache()
                    pred_norm = sliding_window_inference(
                        model, batch, device,
                        patch_size=FALLBACK_PATCH_SIZE,
                        overlap=FALLBACK_OVERLAP
                    )
                else:
                    raise e

            # --- Post-processing ---
            pred_norm = torch.clamp(pred_norm, 0.0, 1.0)
            boundary_mask = batch['BoundaryMask'].to(device)
            valid_tensor = torch.from_numpy(valid_mask_numpy).to(device)

            # Inverse normalization
            pred_real = inverse_normalize(pred_norm, 'Label')
            target_real = inverse_normalize(batch['Label'].to(device), 'Label')

            # Apply mask (Boundary & DEM Valid)
            final_mask = (boundary_mask > 0.5) & valid_tensor

            pred_real[~final_mask] = float('nan')
            target_real[~final_mask] = float('nan')

            # Physical range filtering (-50 ~ 60)
            pred_real[(pred_real < -50) | (pred_real > 60)] = float('nan')

            # --- Calculate Grid Metrics (vs RF) ---
            p_flat = pred_real.cpu().numpy().flatten()
            t_flat = target_real.cpu().numpy().flatten()
            mask = ~np.isnan(p_flat) & ~np.isnan(t_flat)

            if mask.sum() > 0:
                rmse, mae, r2, _ = calculate_metrics(t_flat[mask], p_flat[mask])
                grid_metrics['rmse'].append(rmse)
                grid_metrics['mae'].append(mae)
                grid_metrics['r2'].append(r2)

            # --- Save TIF ---
            tif_name = f"Pred_T2m_{current_date}.tif"
            save_geotiff(os.path.join(TIF_OUTPUT_DIR, tif_name),
                         pred_real.squeeze().cpu().numpy(), ref_profile)

            # --- Simple visualization (every 10 days) ---
            if i % 10 == 0:
                save_visualization(current_date, pred_real, target_real, OUTPUT_DIR)

            # --- Collect station validation data ---
            if 'StationVal' in batch:
                st_val_norm = batch['StationVal'].to(device)
                st_mask = batch['StationMask'].to(device)

                # Stations must be within valid area
                valid_st = (st_mask > 0.5) & final_mask

                if valid_st.sum() > 0:
                    st_obs = inverse_normalize(st_val_norm, 'Label')[valid_st]
                    st_pred = pred_real[valid_st]

                    station_data['obs'].append(st_obs.cpu().numpy())
                    station_data['pred'].append(st_pred.cpu().numpy())

    # ==========================================
    # 5. Results Summary
    # ==========================================
    print("\n" + "=" * 40)
    print("           TEST RESULTS           ")
    print("=" * 40)

    if grid_metrics['rmse']:
        print(f"🌍 [Grid vs RF Label] Average:")
        print(f"   RMSE: {np.mean(grid_metrics['rmse']):.4f}")
        print(f"   MAE : {np.mean(grid_metrics['mae']):.4f}")
        print(f"   R²  : {np.mean(grid_metrics['r2']):.4f}")

    if station_data['obs']:
        all_obs = np.concatenate(station_data['obs'])
        all_pred = np.concatenate(station_data['pred'])

        valid = ~np.isnan(all_obs) & ~np.isnan(all_pred)
        all_obs, all_pred = all_obs[valid], all_pred[valid]

        rmse, mae, r2, pcc = calculate_metrics(all_obs, all_pred)
        print("-" * 40)
        print(f"🔭 [Station Validation] ({len(all_obs)} points):")
        print(f"   RMSE: {rmse:.4f}")
        print(f"   MAE : {mae:.4f}")
        print(f"   R²  : {r2:.4f}")
        print(f"   PCC : {pcc:.4f}")

        plot_station_scatter(all_obs, all_pred, STATION_SCATTER_PATH)
        print(f"📊 Scatter plot saved to: {STATION_SCATTER_PATH}")

    print("=" * 40)
    print(f"✅ Testing Complete. TIFs saved in: {TIF_OUTPUT_DIR}")


if __name__ == '__main__':
    main_test()