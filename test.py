def full_image_inference(model, batch_data, device):
    """
    Attempt to infer directly on full image
    """
    model.eval()
    inputs = {}
    # Exclude non-input features
    exclude_keys = ['StationMask', 'StationVal', 'BoundaryMask', 'Label', 'T2m']

    for k, v in batch_data.items():
        if k not in exclude_keys:
            inputs[k] = v.to(device)

    # Direct forward propagation
    return model(inputs)


def sliding_window_inference(model, batch_data, device, patch_size=512, overlap=64):
    """
    Sliding window inference (Fallback)
    """
    ref = next(iter(batch_data.values()))
    _, _, H, W = ref.shape

    output_full = torch.zeros((1, 1, H, W), device=device)
    weight_map_full = torch.zeros((1, 1, H, W), device=device)

    # Create Hanning window for smooth tiling
    d_window = torch.hann_window(patch_size, periodic=False, device=device)
    window_2d = (d_window.unsqueeze(0) * d_window.unsqueeze(1)).view(1, 1, patch_size, patch_size)

    stride = patch_size - overlap
    y_starts = list(range(0, H - patch_size + 1, stride))
    if y_starts[-1] + patch_size < H: y_starts.append(H - patch_size)
    x_starts = list(range(0, W - patch_size + 1, stride))
    if x_starts[-1] + patch_size < W: x_starts.append(W - patch_size)

    exclude_keys = ['StationMask', 'StationVal', 'BoundaryMask', 'Label', 'T2m']

    model.eval()
    with torch.no_grad():
        for y in y_starts:
            for x in x_starts:
                patch_batch = {}
                for k, v in batch_data.items():
                    if k not in exclude_keys:
                        patch_batch[k] = v[:, :, y:y + patch_size, x:x + patch_size].to(device)

                pred_patch = model(patch_batch)
                output_full[:, :, y:y + patch_size, x:x + patch_size] += pred_patch * window_2d
                weight_map_full[:, :, y:y + patch_size, x:x + patch_size] += window_2d

    output_full /= (weight_map_full + 1e-6)
    return output_full


